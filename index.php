Da febbraio 2010 a giugno 2011 le viene affidata la conduzione di uno dei piu’ popolari  programmi musicali internazionali, Top of the Pops. 
</p><p>
Dal 2012 si sperimenta come giornalista interagendo con professionisti e personaggi di spicco della politica e dello spettacolo italiano.
